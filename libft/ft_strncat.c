#include <string.h>

char	*ft_strncat(char *s1, const char *s2, size_t n)
{
	char *t1;
	char *t2;

	t1 = s1;
	t2 = (char*)s2;
	while (*t1)
		t1++;
	while (*t2 && n--)
		*t1++ = *t2++;
	*t1 = 0;
	return (s1);
}
