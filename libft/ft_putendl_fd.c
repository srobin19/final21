#include <unistd.h>

void	ft_putendl_fd(char const *s, int fd)
{
	int		i;
	char	nl;

	nl = '\n';
	if (!s)
		return ;
	i = 0;
	while (s[i])
	{
		write(fd, &s[i], 1);
		i++;
	}
	write(fd, &nl, 1);
}
