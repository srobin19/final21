#include <unistd.h>

void	ft_putendl(char const *s)
{
	int		i;
	char	nl;

	nl = '\n';
	if (!s)
		return ;
	i = 0;
	while (s[i])
	{
		write(1, &s[i], 1);
		i++;
	}
	write(1, &nl, 1);
}
